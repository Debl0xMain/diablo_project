        <p class="saison_text col text-start">il y a deux nouvelles fonctionnalités durant la saison,<br> le Passe de combat, et le Périple. On connaît leur fonctionnement général,<br>mais les éléments spécifiques, comme la liste des objectifs et des récompenses restent à venir. <br> En attendant leurs guides dédiés, voici un rapide résumé</p>
        <ul class="saison_text_ul my-3">
            <li>Le Périple sert de tableau de progression dans la saison. Il est composé de 7 chapitres à la difficulté croissante.</li>
            <li>Il faut remplir un certain nombre d'objectifs de chaque chapitre. Mais vous pouvez en négliger certains, comme ceux de type JcJ, il ne vous en faut que 7 sur 9 à chaque fois.</li>
            <li>Va avoir des objets en récompense, des Aspects autrement impossibles à obtenir.</li>
            <li>Un Parchemin d'amnésie est obtenu à la fin du Périple, il permet de réinitialiser tous les points de parangon et de talent d'un personnage.</li>
            <li>Il n'y a pas besoin de payer le Passe de combat pour avoir accès au Périple.</li>
        </ul>
